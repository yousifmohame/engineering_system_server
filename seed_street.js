async function main() {
  console.log("🌱 بدء عملية حقن بيانات قطاعات وأحياء الرياض (Seeding)...");

  // لتتبع الأحياء التي تم إضافتها ومنع التكرار (لأن بعض الأحياء ذُكرت في قطاعين مثل النفل والروضة)
  const processedDistricts = new Set();
  let totalDistrictsAdded = 0;

  for (const sectorData of riyadhData) {
    // ===================================================
    // 1. إضافة القطاع (Sector)
    // ===================================================
    const sector = await prisma.riyadhSector.upsert({
      where: { name: sectorData.name },
      update: {}, 
      create: {
        name: sectorData.name,
        code: sectorData.code,
      },
    });
    console.log(`\n🏢 تم معالجة القطاع: ${sector.name}`);

    // ===================================================
    // 2. إضافة الأحياء التابعة (Districts)
    // ===================================================
    let districtCounter = 1;

    for (const districtName of sectorData.districts) {
      const cleanName = districtName.trim();

      // التحقق من عدم التكرار
      if (!processedDistricts.has(cleanName)) {
        
        // توليد كود فريد للحي مثل: NBH-SEC-C-001
        const districtCode = `NBH-${sectorData.code}-${String(districtCounter).padStart(3, '0')}`;

        await prisma.riyadhDistrict.upsert({
          where: { name: cleanName },
          update: {
            sectorId: sector.id // تحديث الربط بالقطاع في حال تم نقله
          },
          create: {
            name: cleanName,
            code: districtCode,
            sectorId: sector.id, 
          },
        });

        processedDistricts.add(cleanName);
        districtCounter++;
        totalDistrictsAdded++;
      }
    }
    console.log(`✅ تمت إضافة ${districtCounter - 1} حياً جديداً في ${sector.name}`);
  }

  console.log(`\n🎉 تم الانتهاء بنجاح! إجمالي الأحياء الفريدة التي تم تسجيلها: ${totalDistrictsAdded}`);
}

main()
  .catch((e) => {
    console.error("❌ حدث خطأ أثناء الحقن:", e);
    process.exit(1);
  })
  .finally(async () => {
    await prisma.$disconnect();
  });